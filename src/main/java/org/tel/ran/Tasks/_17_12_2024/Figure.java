package org.tel.ran.Tasks._17_12_2024;

public class Figure {
    private int width;
    private double area;

    public Figure(int width) {
        this.width = width;
    }
    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

}
